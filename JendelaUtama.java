    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (btnDelete.getText().equals("Delete")) {
            int dialog = JOptionPane.showConfirmDialog(this, "Apakah Anda Yakin Akan Menghapus Data Ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            MahasiswaManager mhsmgr = new MahasiswaManager();
            Mahasiswa m = mahasiswa.get(currentRow);
                if (dialog == JOptionPane.YES_NO_OPTION) {
                    if (mhsmgr.Delete(m)>0){
                        loadData();
                        currentRow = currentRow - 1;
                        binData();
                        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus","Informasi", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    JOptionPane.showMessageDialog(this, "Data Gagal Dihapus","Informasi", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            } else if (btnDelete.getText().equals("Batal")) {
                loadData();
                binData();
                btnNew.setText("Baru");
                btnEdit.setText("Ubah");
                btnDelete.setText("Hapus");
                btnEdit.setEnabled(true);
                btnNew.setEnabled(true);
                btnPrev.setEnabled(true);
                btnNext.setEnabled(true);
                txtNo_BP.setEditable(false);
                txtNama.setEditable(false);
                txtTempat_Lahir.setEditable(false);
                txtTanggal_Lahir.setEditable(false);
                txtAlamat.setEditable(false);
                txtTelp.setEditable(false);
                txtAsal_Sekolah.setEditable(false);
            }
// TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        if(btnNew.getText().equals("New")){
            txtNo_BP.setText("");
            txtNama.setText("");
            txtTempat_Lahir.setText("");
            txtTanggal_Lahir.setText("");
            txtAlamat.setText("");
            txtTelp.setText("");
            txtAsal_Sekolah.setText("");
            txtNo_BP.setEditable(true);
            txtNama.setEditable(true);
            txtTempat_Lahir.setEditable(true);
            txtTanggal_Lahir.setEditable(true);
            txtAlamat.setEditable(true);
            txtTelp.setEditable(true);
            txtAsal_Sekolah.setEditable(true);
            txtNo_BP.requestFocus();
            btnNew.setText("Simpan");
            btnDelete.setText("Batal");
            btnEdit.setEnabled(false);
            btnPrev.setEnabled(false);
            btnNext.setEnabled(false);
            }else {
                if (!txtNo_BP.getText().equals("") && !txtNama.getText().equals("")){
                    MahasiswaManager mhsmgr = new MahasiswaManager();
                    Mahasiswa m = new Mahasiswa();
                    m.setnobp( txtNo_BP.getText());
                    m.setnama(txtNama.getName());
                    m.settmplahir(txtTempat_Lahir.getText());
                    m.settgllahir(txtTanggal_Lahir.getText());
                    m.setalamat(txtAlamat.getText());
                    m.settlp(txtTelp.getText());
                    m.setasalsekolah(txtAsal_Sekolah.getText());
                    if (mhsmgr.Insert(m)>0) {
                        loadData();
                        currentRow = mahasiswa.size()-1;
                        binData();
                        JOptionPane.showMessageDialog(this,"Data Berhasil Disimpan","Informasi",JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this,"Data Gagal Disimpan","Informasi", JOptionPane.INFORMATION_MESSAGE);
                    }
                    txtNo_BP.setEditable(false);
                    txtNama.setEditable(false);
                    txtTempat_Lahir.setEditable(false);
                    txtTanggal_Lahir.setEditable(false);
                    txtAlamat.setEditable(false);
                    txtTelp.setEditable(false);
                    txtAsal_Sekolah.setEditable(false);
                    btnNew.setText("New");
                    btnDelete.setText("Delete");
                    btnEdit.setEnabled(true);
                    btnPrev.setEnabled(true);
                    btnNext.setEnabled(true);
                    mhsmgr.closeConnection();
            } else {
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan","Informasi", JOptionPane.INFORMATION_MESSAGE);
}
}
    }//GEN-LAST:event_btnNewActionPerformed

