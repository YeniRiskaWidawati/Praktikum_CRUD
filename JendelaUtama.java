    private void loadData(){
        MahasiswaManager mhsmgr = new MahasiswaManager();
        mahasiswa = mhsmgr.getMahasiswa();
        mhsmgr.closeConnection();
    }
    
    private void binData(){
        if (mahasiswa.size()>0){
            Mahasiswa m = mahasiswa.get(currentRow);
            txtNo_BP.setText(m.getnobp());
            txtNama.setText(m.getnama());
            txtTempat_Lahir.setText(m.gettmplahir());
            txtTanggal_Lahir.setText(m.gettgllahir());
            txtAlamat.setText(m.getalamat());
            txtTelp.setText(m.gettlp());
            txtAsal_Sekolah.setText(m.getasalsekolah());
        } else {
            currentRow = 0;
            txtNo_BP.setText("");
            txtNama.setText("");
            txtTempat_Lahir.setText("");
            txtTanggal_Lahir.setText("");
            txtAlamat.setText("");
            txtTelp.setText("");
            txtAsal_Sekolah.setText("");
            }
    }    
    


